package com.fri.pojo.bo.app.push;

public class FacePhoneInfo {
    private String name;
    private String cardNumber;
    private String img;
    private String similarityDegree;
    private String guoJi;

    public String getGuoJi() {
        return guoJi;
    }

    public void setGuoJi(String guoJi) {
        this.guoJi = guoJi;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCardNumber() {
        return cardNumber;
    }

    public void setCardNumber(String cardNumber) {
        this.cardNumber = cardNumber;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getSimilarityDegree() {
        return similarityDegree;
    }

    public void setSimilarityDegree(String similarityDegree) {
        this.similarityDegree = similarityDegree;
    }
}
